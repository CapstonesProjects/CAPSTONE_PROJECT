            <h1 class="section-title">Section 2:  Discipline </h1>

            <h2 class="sub-sec">A.	Categories of Sanctions</h2>
                <section class="sec-info">
                <p class="para-sent">Disciplinary Administrative Sanctions for purposes of student discipline for Major Offenses 
                    that may be applied upon an erring student are Suspension, Exclusion and Expulsion.</p>
            </section>

            <section class="right">
            <ul>
                <li>SUSPENSION - A penalty allowed by Commission on Higher Education(CHED), where 
                    the school deprives an erring student, a period not exceeding twenty percent (20%) of 
                    attendance in class for the entire semester thus:</li>
                <li>The Off - School Suspension is when the student is expected to stay home during the suspension.</li>
                <li>The In - School Suspension is when the student is asked to come to the school to 
                    attend extra-curricular activities involving community services within the school.</li>
            </ul>
            </section>

            <h2 class="sub-note">Note: </h2>
                <section class="sec-note">
                <p class="para-note">Students under these suspensions are NOT ALLOWED to attend classes.</p>
            </section>

            <section class="right">
            <ul>
                <li>EXCLUSION - A disciplinary measure where the school must bar or restrict a 
                    student from entering the school premises and non-admission in the Institution due 
                    to commission of a major offense subject to due process. </li>
                <li>EXPULSION - An extreme administrative sanction debarring offending student 
                    from all public and private schools. This requires the approval of the Commission on 
                    Higher Education (CHED). </li>
            </ul>
            </section>

